#10. –î–∞–Ω –Ω–µ–æ—Ä–∏–µ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –≥—Ä–∞—Ñ. –£–¥–∞–ª–∏—Ç—å —Ä–µ–±—Ä–æ, —Å–æ–µ–¥–∏–Ω—è—é—â–µ–µ –≤–µ—Ä—à–∏–Ω—ã ùëé –∏ ùëè.


import networkx as nx
import matplotlib.pyplot as plt

def remove_edge(graph, a, b):
    graph.remove_edge(a, b)

# –°–æ–∑–¥–∞–Ω–∏–µ –≥—Ä–∞—Ñ–∞
graph = nx.Graph()
graph.add_edge(1, 2)
graph.add_edge(2, 3)
graph.add_edge(2, 4)
graph.add_edge(3, 5)
graph.add_edge(1, 3)

#edges = [(1, 2), (1, 3), (2, 3), (2, 4), (3, 5), (5, 5)]
# –í–∏–∑—É–∞–ª–∏–∑–∞—Ü–∏—è –∏—Å—Ö–æ–¥–Ω–æ–≥–æ –≥—Ä–∞—Ñ–∞
pos = nx.spring_layout(graph)  # –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –ø–æ–∑–∏—Ü–∏–π –≤–µ—Ä—à–∏–Ω –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è
nx.draw(graph, pos, with_labels=True)  # –û—Ç—Ä–∏—Å–æ–≤–∫–∞ –≥—Ä–∞—Ñ–∞
plt.title('–ò—Å—Ö–æ–¥–Ω—ã–π –≥—Ä–∞—Ñ')
plt.show()

# –£–¥–∞–ª–µ–Ω–∏–µ —Ä–µ–±—Ä–∞ –∏ –≤–∏–∑—É–∞–ª–∏–∑–∞—Ü–∏—è –∏–∑–º–µ–Ω–µ–Ω–Ω–æ–≥–æ –≥—Ä–∞—Ñ–∞
remove_edge(graph, 2, 3)

nx.draw(graph, pos, with_labels=True)  # –û—Ç—Ä–∏—Å–æ–≤–∫–∞ –≥—Ä–∞—Ñ–∞ –ø–æ—Å–ª–µ —É–¥–∞–ª–µ–Ω–∏—è —Ä–µ–±—Ä–∞
plt.title('–ì—Ä–∞—Ñ –ø–æ—Å–ª–µ —É–¥–∞–ª–µ–Ω–∏—è —Ä–µ–±—Ä–∞ (2, 3)')
plt.show()